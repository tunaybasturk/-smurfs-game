package Labirent;


public class Obje {
  private  int altinsayisi;
  private   int mantarsayisi;

    public Obje() {
    }

    public Obje(int altinsayisi, int mantarsayisi) {
        this.altinsayisi = altinsayisi;
        this.mantarsayisi = mantarsayisi;
    }

    public int getAltinsayisi() {
        return altinsayisi;
    }

    public void setAltinsayisi(int altinsayisi) {
        this.altinsayisi = altinsayisi;
    }

    public int getMantarsayisi() {
        return mantarsayisi;
    }

    public void setMantarsayisi(int mantarsayisi) {
        this.mantarsayisi = mantarsayisi;
    }
  
    
    
}
